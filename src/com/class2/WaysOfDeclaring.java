package com.class2;

public class WaysOfDeclaring {

	public static void main(String[] args) {
		// different ways to create/declare variables;
		// 1. create variable and assign value
		int num = 28;

		// 2 ways: using 2 steps
		int y; // 1. declare variable
		y = 12; // 2. assign value

		// declare 1 by 1
		int a;
		int b;
		int c;

		// or declare all together
		int d, e, f, g;

		a = 3;
		b = 77;
		f = 43;
		g = 25;

		// ctrl+shift+f = formatting

	}

}
