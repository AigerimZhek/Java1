package com.class7;

public class Loop {

	public static void main(String[] args) {
		
		int a=1;
		while(a<10) {
			System.out.println("Good morning");
			a++;
		}
		
		
		int b=1;
		while (b<=5) {
			System.out.println("I am inside while loop "+b);
			b++;
		}
		System.out.println("i am outside of while loop");
		
		
		int z=10;
		while(z<=20) {
			System.out.println(z);
			z++;
		}
		
		
		int y=1;
		while(y<=20) {
			System.out.print(y+" ");
			y++;
		}
		
		
		System.out.println(" ");
		int e=10;
		while(e>=1) {
			System.out.print(e+" ");
			e--;
		}
	}
}
