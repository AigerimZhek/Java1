package com.class7;

public class IncrementAndDecrement {

	public static void main(String[] args) {
		
		//post increment x++
		int a=299;
		int b=a++;
		
		System.out.println(a);//300
		System.out.println(b);//299
		
		//post decrement x--
		
		int x=10;
		int y=x--;
		
		System.out.println(x);//9
		System.out.println(y);//10

	}

}
